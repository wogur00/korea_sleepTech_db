# 트리거 실전 활용 예시 #
/*
1) 회원 탈퇴 시 로그 기록
	>> AFTER DELETE 트리거로 log_table에 기록
    
2) 상품 재고가 0이 되면 품절 처리	
	>> AFTER UPDATE 트리거로 품절 플래그 수정
    
    cf) 플래그(Flag): 어떤 상태를 구분하기 위한 표시용 값
		EX) 품절 플래그: 상품이 품절인지 아닌지를 나타내는 컬럼 (is_sold_out)
        
        stock 재고 수량 
        is_sold_out 품절 여부 (0-판매중, 1-품절됨)
        
        >> 재고 수량이 0이 되면 자동으로 품절 상태를 1로 변경 (after update 트리거)
    
3) 주문 시 자동으로 포인트 적립	
	>> AFTER INSERT 트리거로 포인트 계산 후 적립
*/

